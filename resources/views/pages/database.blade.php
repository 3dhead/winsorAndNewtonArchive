        <h1>The Database</h1>
        <p>Digital images of pigment, paint, varnish and oil recipes from the nineteenth century archive of Winsor &amp; Newton, artists’ colourmen, have been captured and made accessible by an innovatively structured database. A new database approach has been developed for making the contents of each manuscript easily accessible without the need for exhaustive transcription or complex editing. The database displays full images of each hand-written page from the archive alongside an electronic index and summary of individual recipe contents. This removes the problems of full transcription, and allows the rapid generation of indices. Since the original page is always visible to the user, subsequent researchers need not be restricted to interpretations made by the individuals creating the database. Fresh interpretations of the content of the text can continue, unlike conventional publications or databases where the information is filtered through the original editors.</p>
        <h2>Design</h2>
        <p>The database fields include subject classifications and a summary of the information from each recipe. Researchers can search for specific recipes or for specific materials. Chronological and alphabetical sorts are integral to the search functions. To facilitate searches in detailed records, all materials mentioned in a given recipe or manufacturing description are entered, using both the original wording and an interpreted description of the material to clarify obsolete words, difficult words, code words or abbreviations (e.g. "muriatic acid" = hydrochloric acid). </p>
        <div class="c_image">
            <img src="/images/archives/wn/dictionary.gif" alt="screenshot of dictionary" width="520px" />
            <p>Screenshot of a record from the dictionary database</p>
        </div>
        <p>Throughout the archive abbreviations have been used frequently with the original meaning often appearing in only one location. Therefore the full interpretation of an abbreviation would be extremely difficult to find without an electronic database. In detailed records, recipes are indexed by quantities and proportions of materials, to enable variations or similarities in preparation to be identified. British measures are displayed side by side with international units, a feature which has been automated for data-entry.</p>
        <p>The first part of the project (2004-5) focused on the part of the archive specifically dealing with oil painting materials (approximately 10,700 pages, nearly 10,000 individual ’recipe’ items). The second part (2006-7) improved the quality of the images, increasing the detail of the indexing and will add further material, on watercolour (approximately 6,300 pages), and nineteenth century catalogues (approximately 5000 pages).  The first part of the complete database with images may be used after successfully  <a href="application.php">applying for access</a>. </p>
        <h2>Examples of Full Records<a name="example"></a></h2>
        <p>Some of the archive may be commercially sensitive so only an outline of the complete information is provided here in the index database, and no page-images have been included.  To give an idea of what the full database contains, here are some examples of full records and images which are not sensitive.</p>
        <p>In the full database each recipe record begins with the title as it appears in the manuscript, and a new title that describes the text in modern terminology, with an indication of the broad topics covered.</p>
        <div class="c_image">
            <img src="/images/archives/wn/titlebar.gif" alt="screenshot of recipe title" width="653px"/>
            <p>Screenshot of a the title area of a record in the full database</p>
        </div>
        <p>Then the materials are tabulated, both in the original terminology and quantities, and in a modern interpretation.</p>
        <div class="c_image">
            <img src="/images/archives/wn/ingredients_table.gif" alt="screenshot of ingredients list" width="651px"/>
            <p>Screenshot of a record from the ingredients table of the full database</p>
        </div>
        <p>Further fields record the date of the recipe, any named individuals or companies, whether it is illustrated or contains a painted sample, the materials from which any specified equipment is made, and so on. Further fields provide space for a process summary, any partial or complete transcriptions, and notes and queries. </p>
        <p>The materials terminology is contained within a related database which links original terms to modern interpretations, and provides a justification, either from within the archive (cross-references) or from external literature.</p>
